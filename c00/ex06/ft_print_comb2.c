#include<unistd.h>

void	print(char a)
{
	write(1, &a, 1);
}

void	ft_print_comb2(void)
{
	int	q;
	int	a;

	q = 0;
	while (q <= 98)
	{
		a = q + 1;
		while (a < 100)
		{
			print((q / 10) + '0');
			print((q % 10) + '0');
			print(' ');
			print((a / 10) + '0');
			print((a % 10) + '0');
			if (q != 98)
			{
				write(1, ", ", 2);
			}
			a++;
		}
		q++;
	}
}
