int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (--i >= 1)
	{
		nb = nb * i;
	}
	return (nb);
}
